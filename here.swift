import Foundation

func readIntegers() -> [Int] {
    var integers = [Int]()
    
    while let input = readLine(), let integer = Int(input) {
        integers += [integer]
    }
    
    return integers
}

func printIntegers(integers: [Int]) {
    for integer in integers {
        print(integer)
    }
}
